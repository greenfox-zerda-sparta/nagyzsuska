#include <iostream>
#include <string>

using namespace std;

int main() {

	int ad = 6;
	int a = 0;
	do {
		cout << a << endl;
		a++;
	} while (a<=ad);

	int ae = 6;
	int b = 0;
	while (b<=ae){
		cout << b << endl;
		b++;
	}

	int af = 6;
	for (int a = 0;  a <= af; a++) {
		cout << a << endl;
	}

	return 0;
}
